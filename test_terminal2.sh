#!/bin/bash
# Очистка экрана
echo -e "\033[2J\033[H"

# Рисуем рамку 8x8 начиная с строки 10, столбца 5
# Верхняя линия
echo -e "\033[10;5H\033(0lqqqqqqk\033(B"
# Боковые линии (6 внутренних строк)
echo -e "\033[11;5H\033(0x\033(B      \033(0x\033(B"
echo -e "\033[12;5H\033(0x\033(B      \033(0x\033(B"
echo -e "\033[13;5H\033(0x\033(B      \033(0x\033(B"
echo -e "\033[14;5H\033(0x\033(B      \033(0x\033(B"
echo -e "\033[15;5H\033(0x\033(B      \033(0x\033(B"
echo -e "\033[16;5H\033(0x\033(B      \033(0x\033(B"
# Нижняя линия
echo -e "\033[17;5H\033(0mqqqqqqj\033(B"

# Рисуем цифру 3 внутри рамки (ACS_CKBOARD = 'a' в alt charset)
# Паттерн цифры 3:
#  xxxxxx
#       x
#  xxxxxx
#       x
#       x
#  xxxxxx
echo -e "\033[11;6H\033(0aaaaaa\033(B"
echo -e "\033[12;11H\033(0a\033(B"
echo -e "\033[13;6H\033(0aaaaaa\033(B"
echo -e "\033[14;11H\033(0a\033(B"
echo -e "\033[15;11H\033(0a\033(B"
echo -e "\033[16;6H\033(0aaaaaa\033(B"

echo -e "\033[19;1H\033[0m"